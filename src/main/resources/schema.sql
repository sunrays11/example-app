create table student(  
  studentid    int,  
  name         varchar(100),  
  classid      varchar(9),  
  maths        decimal(5,2),
  science      decimal(5,2),
  lang         decimal(5,2),  
  average      decimal(5,2),  
  grades        varchar(9),
  constraint pk_student primary key (studentid),  
);








